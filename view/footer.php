	<script type="text/javascript">
		$(document).ready(function(){
			show_cart();
			load_cart_data();


			// in sản phẩm ở giỏ hàng sidebar

			function load_cart_data(){
				$.ajax({
					url:"app/ajax/fetch_cart_sidebar.php",
					method:"POST",
					dataType:"json",
					success:function(data){
						$('#cart_details').html(data.cart_details);
						$('#total_price').text(data.total_price);
						$('#badge').text(data.total_item);
					
					}
				});
			};
			// in sản phẩm ở giỏ hàng page 
			function show_cart(){
				$.ajax({
					url:"app/ajax/fetch_cart_page.php",
					method:"POST",
					dataType:"json",
					success:function(data){
						$('#show_cart').html(data.show_cart);
						$('#total_cart').text(data.total_cart);
	
					}
				});
			};
			// Sửa sản phẩm
			$(document).on('change','.itemQty',function(){

		      	var $el = $(this).closest('tr');

		      	var product_id = $el.find('.pid').val();
		      	var product_quantity = $el.find('.itemQty').val();
		     	var action = 'update';
		   
		      // location.reload('true');
		      	console.log(product_quantity);
		      	console.log(product_id);
		      	$.ajax({
		        	url:'app/ajax/cart.php',
		        	method:'POST',
		        	data:{product_quantity:product_quantity,product_id:product_id,action:action},
		        	success:function(){
		   				load_cart_data();
		          		show_cart();
						
		        	}
		      	})
		    });

		

			

			
			

			// thêm sản phẩm
			$(document).on('click', '.add_to_cart', function(){

				var product_id = $(this).attr("id");
				var product_name = $('#name'+product_id+'').val();
				var product_img = $('#img'+product_id+'').val();
				var product_price = $('#price'+product_id+'').val();
				var product_quantity = $('#quantity'+product_id+'').val();
				var product_color = $('#color'+product_id+'').val();
				var product_size = $('#size'+product_id+'').val();
				var action = 'add';
				$.ajax({
					url:"app/ajax/cart.php",
					method:"POST",
					data:{product_id:product_id, product_name:product_name, product_img:product_img, product_price:product_price, product_quantity:product_quantity, product_color:product_color, product_size:product_size, action:action},
					success:function(data){
						
						load_cart_data();
						
					}
				})
			})
			
			// Xoá sản phẩm trong giỏ hàng
			$(document).on('click', '.delete', function(){
				var product_id = $(this).attr("id");
				var action = "remove";
				$.ajax({
					url:"app/ajax/cart.php",
					method:"POST",
					data:{product_id:product_id, action:action},
					success:function(){
						
						load_cart_data();
						show_cart();
						
					}
				});
			});


		});
	</script>

